console.log("Background Service Worker Active and Listening...");

chrome.runtime.onMessage.addListener((request, sender, sendResponse) => {
  if (request.action === "CAPTURE_TAB") {
    try {
      chrome.tabs.captureVisibleTab(null, { format: "png" }, (dataUrl) => {
        if (chrome.runtime.lastError) {
          sendResponse({ status: "error", message: chrome.runtime.lastError.message });
        } else if (!dataUrl) {
          sendResponse({ status: "error", message: "No data image stream generated by Chrome." });
        } else {
          sendResponse({ status: "success", dataUrl: dataUrl });
        }
      });
    } catch (err) {
      sendResponse({ status: "error", message: err.message });
    }
    return true; // Keep message channel open asynchronously
  }
});